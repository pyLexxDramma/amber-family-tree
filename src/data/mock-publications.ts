const photo = (id: number, file: string, name: string, pubId?: string): MediaItem => {
  const url = pubId && GENERATED_POSTS[pubId] ? GENERATED_POSTS[pubId] : feedImage(id);
  return {
    id: `ph${id}`,
    type: 'photo',
    url,
    thumbnail: url,
    name,
    size: 2_800_000 + id * 10_000,
  };
};

